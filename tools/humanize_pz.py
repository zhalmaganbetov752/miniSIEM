# 4. Основной запуск
# ---------------------------------------------------------------------------
def main() -> None:
    if not PZ.exists():
        print(f"[!] Не найден {PZ}")
        return

    # Backup
    if not BACKUP.exists():
        shutil.copy2(PZ, BACKUP)
        print(f"[+] Backup: {BACKUP.name}")
    else:
        print(f"[i] Backup уже существует: {BACKUP.name}")

    doc = Document(str(PZ))
    paragraphs = doc.paragraphs

    # 1) Полные перезаписи
    full_rewrites_done = 0
    for idx, new_text in PARA_REWRITES.items():
        if 0 <= idx < len(paragraphs):
            old_text = get_paragraph_text(paragraphs[idx])
            if old_text.strip():
                replace_paragraph_text(paragraphs[idx], new_text)
                full_rewrites_done += 1
            else:
                print(f"  [skip] Параграф {idx} пуст, пропускаю")
        else:
            print(f"  [skip] Параграф {idx} вне диапазона ({len(paragraphs)})")
    print(f"[+] Переписано {full_rewrites_done} ключевых параграфов")

    # 2) Глобальные замены AI-маркерных фраз
    rewritten_indices = set(PARA_REWRITES.keys())
    global_changes = 0
    for i, p in enumerate(paragraphs):
        if i in rewritten_indices:
            continue  # уже переписан полностью
        original = get_paragraph_text(p)
        if not original.strip() or len(original) < 5:
            continue
        new = apply_global_replacements(original)
        if new != original:
            replace_paragraph_text(p, new)
            global_changes += 1
    print(f"[+] Глобальные замены применены к {global_changes} параграфам")

    # Сохранение
    try:
        doc.save(str(PZ))
        print(f"[+] Сохранено: {PZ.name}")
    except PermissionError:
        doc.save(str(ALT))
        print(
            f"[!] {PZ.name} занят (открыт в Word). "
            f"Сохранил в {ALT.name}. "
            "Закрой Word и переименуй файл вручную."
        )

    # Статистика по новому файлу
    final = Document(str(PZ if PZ.exists() else ALT))
    chars = sum(
        len("".join(r.text for r in p.runs))
        for p in final.paragraphs
    )
    print(f"[i] Итог: {chars} символов, ~{chars // 1900} страниц")


if __name__ == "__main__":
    main()
