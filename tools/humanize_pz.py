def replace_paragraph_text(p: Paragraph, new_text: str) -> None:
    """Заменяет весь текст параграфа, сохраняя форматирование первого run."""
    runs = p.runs
    if not runs:
        # Параграф без runs — добавим run с дефолтным стилем
        p.add_run(new_text)
        return
    # Сохраняем первый run, его текст заменяем на new_text
    first = runs[0]
    first.text = new_text
    # Остальные runs обнуляем
    for r in runs[1:]:
        r.text = ""


def get_paragraph_text(p: Paragraph) -> str:
    return "".join(r.text for r in p.runs)


def apply_global_replacements(text: str) -> str:
    if not text or len(text) < 4:
        return text
    out = text
    for pattern, repl in GLOBAL_REPLACEMENTS:
        out = re.sub(pattern, repl, out)
    return out


