from docx.oxml import OxmlElement
from docx.text.paragraph import Paragraph

DOC_PATH = Path(__file__).resolve().parent / "ПЗ Жалмаганбетов — копия.docx"

stats = {"dup": 0, "para": 0, "phrase": 0, "insert": 0}


def set_paragraph_text(paragraph, text: str) -> None:
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def insert_paragraph_after(paragraph, text: str) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    new_para.add_run(text)
    return new_para


def set_multiline_paragraph(paragraph, block: str) -> Paragraph:
    lines = [ln.strip() for ln in block.split("\n") if ln.strip()]
    if not lines:
        return paragraph
    set_paragraph_text(paragraph, lines[0])
    cur = paragraph
    for line in lines[1:]:
        cur = insert_paragraph_after(cur, line)
    return cur


def iter_paragraphs(doc):
    for p in doc.paragraphs:
        yield p
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for p in cell.paragraphs:
                    yield p


def replace_nth_containing(doc, needle: str, new_text: str, n: int = 2) -> bool:
    occ = 0
    for p in iter_paragraphs(doc):
        if needle in p.text:
            occ += 1
            if occ == n:
                set_paragraph_text(p, new_text)
                stats["dup"] += 1
                return True
    return False


def replace_first_startswith(doc, prefix: str, new_block: str) -> bool:
    for p in iter_paragraphs(doc):
        t = p.text.strip()
        if t.startswith(prefix) or prefix in t[: max(len(prefix), 80)]:
            set_multiline_paragraph(p, new_block)
            stats["para"] += 1
            return True
    return False


def replace_phrases(doc, pairs: list[tuple[str, str]]) -> None:
    for old, new in pairs:
        if not old or old == new:
            continue
        for p in iter_paragraphs(doc):
            if old in p.text:
                t = p.text.replace(old, new)
                set_paragraph_text(p, t)
                stats["phrase"] += 1


def insert_after_anchor(doc, anchor: str, text: str) -> bool:
    for p in doc.paragraphs:
        if anchor in p.text:
            insert_paragraph_after(p, text)
            stats["insert"] += 1
            return True
    return False


