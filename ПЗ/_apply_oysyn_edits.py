def main():
    if not DOC_PATH.exists():
        raise SystemExit(f"File not found: {DOC_PATH}")

    doc = Document(str(DOC_PATH))
    missing = []

    for needle, new in DUP_REPHRASE:
        if not replace_nth_containing(doc, needle, new, n=2):
            # try only occurrence if single
            if not replace_nth_containing(doc, needle, new, n=1):
                missing.append(f"DUP: {needle[:50]}")

    for prefix, block in PARA_REWRITE:
        if not replace_first_startswith(doc, prefix, block):
            missing.append(f"PARA: {prefix[:50]}")

    PHRASES.sort(key=lambda x: len(x[0]), reverse=True)
    replace_phrases(doc, PHRASES)
    replace_phrases(doc, INLINE_SPLITS)

    for anchor, text in INSERTS:
        if not insert_after_anchor(doc, anchor, text):
            missing.append(f"INS: {anchor[:50]}")

    doc.save(str(DOC_PATH))
    print("Saved:", DOC_PATH)
    print("Stats:", stats)
    if missing:
        print("Not found (%d):" % len(missing))
